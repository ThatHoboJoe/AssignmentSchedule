package Schedule;

public class Assignment {

	public String name;
	public int day;
	public int month;
	public int year;
	public int hour;
	public int minute;
	public int second;
	
	public Assignment(String name, int day, int month, int year, int hour, int minute, int second) {
		this.name = name;
		this.day = day;
		this.month = month;
		this.year = year;
		this.hour = hour;
		this.minute = minute;
		this.second = second;
	}
	
	
	
}
